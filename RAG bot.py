import chromadb
from chromadb.utils import embedding_functions

embed_fn = embedding_functions.SentenceTransformerEmbeddingFunction(
    model_name="all-MiniLM-L6-v2"
)

client = chromadb.Client()

# Fix for stale answers — delete old collection if it exists
try:
    client.delete_collection("my_documents")
    print("Cleared previous collection")
except:
    pass

# Fresh collection every time
collection = client.get_or_create_collection(
    name="my_documents",
    embedding_function=embed_fn
)

# Add in batches — avoids memory issues with large files
batch_size = 100
for i in range(0, len(chunks), batch_size):
    batch = chunks[i:i+batch_size]
    collection.add(
        documents=batch,
        ids=[f"chunk_{j}" for j in range(i, i+len(batch))]
    )
    print(f"Stored chunks {i} to {i+len(batch)-1}...")

print(f"\nDone — {collection.count()} chunks ready to query!")

from google.colab import userdata
from groq import Groq

groq_client = Groq(api_key=userdata.get("test101"))

def ask(question, n_results=5):  # increased from 3 to 5
    results = collection.query(
        query_texts=[question],
        n_results=min(n_results, collection.count())
    )
    context = "\n\n".join(results["documents"][0])

    prompt = f"""Answer the question using ONLY the context below.
Be specific — use exact numbers and values from the data.
If the answer isn't in the context, say "I don't have that information."

Context:
{context}

Question: {question}
Answer:"""

    response = groq_client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=400
    )
    return response.choices[0].message.content

