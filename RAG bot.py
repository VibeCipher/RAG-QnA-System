def extract_chunks(filepath):
    ext = filepath.split(".")[-1].lower()

    if ext == "pdf":
        doc = fitz.open(filepath)
        chunks = []
        total_pages = len(doc)
        print(f"PDF has {total_pages} pages — processing...")

        for i, page in enumerate(doc):
            print(f"Processing page {i+1}/{total_pages}...", end=" ")

            # Try normal text extraction first
            text = page.get_text().strip()

            # If empty — scanned page, use OCR
            if not text:
                try:
                    pix = page.get_pixmap(dpi=200)
                    img = Image.open(io.BytesIO(pix.tobytes("png")))
                    text = pytesseract.image_to_string(img).strip()
                    print(f"OCR → {len(text.split())} words")
                except Exception as e:
                    print(f"OCR failed: {e}")
                    text = ""
            else:
                print(f"text → {len(text.split())} words")

            if text:
                chunks.append(f"Page {i+1}:\n{text}")

        doc.close()
        return chunks

    elif ext == "csv":
        df = pd.read_csv(filepath)
        return chunk_dataframe(df)

    elif ext in ["xlsx", "xls"]:
        df = pd.read_excel(filepath)
        return chunk_dataframe(df)

    else:
        print("Unsupported file type.")
        return []

def chunk_dataframe(df):
    chunks = []
    summary = f"This dataset has {len(df)} rows and {len(df.columns)} columns.\n"
    summary += f"Columns: {', '.join(df.columns.tolist())}\n"
    summary += f"Sample data (first 3 rows):\n{df.head(3).to_string(index=False)}"
    chunks.append(summary)

    for i, row in df.iterrows():
        row_text = " | ".join([f"{col}: {val}" for col, val in row.items()])
        chunks.append(row_text)

        if (i + 1) % 10 == 0:
            batch = df.iloc[max(0, i-9):i+1]
            batch_summary = f"Rows {i-8} to {i+1} summary:\n{batch.to_string(index=False)}"
            chunks.append(batch_summary)

    return chunks

